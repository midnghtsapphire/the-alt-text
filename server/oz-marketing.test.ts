describe('Analytics and Reporting', () => {
  it('should aggregate click stats by date', () => {
    const clicks = [
      { date: '2026-01-29', count: 45 },
      { date: '2026-01-28', count: 38 },
      { date: '2026-01-27', count: 52 },
    ];

    const totalClicks = clicks.reduce((sum, day) => sum + day.count, 0);
    expect(totalClicks).toBe(135);
  });

  it('should calculate average click-through rate', () => {
    const campaigns = [
      { impressions: 1000, clicks: 50 },
      { impressions: 2000, clicks: 80 },
      { impressions: 1500, clicks: 60 },
    ];

    const totalImpressions = campaigns.reduce((sum, c) => sum + c.impressions, 0);
    const totalClicks = campaigns.reduce((sum, c) => sum + c.clicks, 0);
    const avgCTR = (totalClicks / totalImpressions) * 100;

    expect(avgCTR).toBeCloseTo(4.22, 2);
  });

  it('should calculate lifetime value per affiliate', () => {
    const affiliate = {
      totalReferrals: 10,
      activeSubscriptions: 7,
      avgSubscriptionValue: 299,
      avgLifetimeMonths: 12,
      commissionRate: 0.20,
    };

    const ltv = affiliate.activeSubscriptions * 
                affiliate.avgSubscriptionValue * 
                affiliate.avgLifetimeMonths * 
                affiliate.commissionRate;

    // 7 * 299 * 12 * 0.20 = 5023.2
    expect(ltv).toBeCloseTo(5023.2, 1);
  });
});
