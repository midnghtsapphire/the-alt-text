describe('Affiliate Link Tracking', () => {
  it('should track click with device and browser info', () => {
    const clickData = {
      affiliateCode: 'ABC12345',
      url: 'https://thealttext.com?ref=ABC12345',
      device: 'desktop',
      browser: 'chrome',
      timestamp: Date.now(),
    };

    expect(clickData.affiliateCode).toBe('ABC12345');
    expect(clickData.device).toBe('desktop');
    expect(clickData.browser).toBe('chrome');
    expect(clickData.timestamp).toBeGreaterThan(0);
  });

  it('should calculate conversion rate', () => {
    const clicks = 100;
    const conversions = 5;
    const conversionRate = (conversions / clicks) * 100;

    expect(conversionRate).toBe(5);
  });

  it('should calculate commission amount', () => {
    const subscriptionPrice = 299; // Professional tier
    const commissionRate = 0.20; // 20% for Silver tier
    const commission = subscriptionPrice * commissionRate;

    expect(commission).toBeCloseTo(59.8, 1);
  });

  it('should calculate MRR from affiliate', () => {
    const activeSubscriptions = [
      { price: 99, commission: 0.15 },  // Starter
      { price: 299, commission: 0.20 }, // Professional
      { price: 299, commission: 0.20 }, // Professional
      { price: 999, commission: 0.25 }, // Enterprise
    ];

    const totalMRR = activeSubscriptions.reduce(
      (sum, sub) => sum + (sub.price * sub.commission),
      0
    );

    expect(totalMRR).toBeCloseTo(384.2, 1);
  });
});

