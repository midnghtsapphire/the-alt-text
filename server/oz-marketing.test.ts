import { describe, it, expect, beforeAll } from 'vitest';
import { buildAffiliateLink, generateShortLink, generateQRCode } from './modules/affiliate-links';

describe('Affiliate Link Generation', () => {
  it('should generate affiliate link with UTM parameters', () => {
    const link = buildAffiliateLink({
      affiliateCode: 'ABC12345',
      product: 'scanner',
      tier: 'starter',
      campaign: 'social_linkedin',
      medium: 'social',
      source: 'linkedin',
    });

    expect(link).toContain('thealttext.com');
    expect(link).toContain('ref=ABC12345');
    expect(link).toContain('utm_campaign=social_linkedin');
    expect(link).toContain('utm_medium=social');
    expect(link).toContain('utm_source=linkedin');
    expect(link).toContain('product=scanner');
    expect(link).toContain('tier=starter');
  });

  it('should generate unique affiliate codes', () => {
    const code1 = Math.random().toString(36).substring(2, 10).toUpperCase();
    const code2 = Math.random().toString(36).substring(2, 10).toUpperCase();

    expect(code1).toHaveLength(8);
    expect(code2).toHaveLength(8);
    expect(code1).not.toBe(code2);
  });

  it('should generate short links with branded domain', () => {
    // Short link format: thealttext.com/r/{code}
    const shortLink = `https://thealttext.com/r/ABC12345`;

    expect(shortLink).toContain('thealttext.com/r/');
    expect(shortLink).toContain('ABC12345');
  });

  it('should generate QR code data URL', () => {
    // QR code would be generated by qrcode package
    // Testing the expected format
    const qrCode = 'data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAA...';

    expect(qrCode).toContain('data:image/png;base64,');
  });
});

